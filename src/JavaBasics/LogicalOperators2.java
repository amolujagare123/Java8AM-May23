package JavaBasics;

public class LogicalOperators2 {

    public static void main(String[] args) {

        int a = 10;
        int b = 10;

        System.out.println(a==b); // true
        System.out.println(a!=b); //  false
        System.out.println(a<b); // false
        System.out.println(a<=b); // true
        System.out.println(a>=b); // true
        System.out.println(a>b); // false
    }
}
